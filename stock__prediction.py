# stock_lstm_forecast.py

import yfinance as yf
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

from sklearn.preprocessing import MinMaxScaler
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense

# 1. Fetch Stock Data
def fetch_stock_data(ticker, start="2020-01-01", end="2025-01-01"):
    df = yf.download(ticker, start=start, end=end)
    df = df[['Close']]  # Only close price
    df.dropna(inplace=True)
    return df

# 2. Prepare Data for LSTM
def prepare_data(df, lookback=60):
    scaler = MinMaxScaler(feature_range=(0, 1))
    scaled_data = scaler.fit_transform(df)

    X, y = [], []
    for i in range(lookback, len(scaled_data)):
        X.append(scaled_data[i-lookback:i, 0])
        y.append(scaled_data[i, 0])

    X, y = np.array(X), np.array(y)
    X = np.reshape(X, (X.shape[0], X.shape[1], 1))  # (samples, time_steps, features)
    return X, y, scaler

# 3. Build LSTM Model
def build_lstm_model(input_shape):
    model = Sequential()
    model.add(LSTM(units=50, return_sequences=True, input_shape=input_shape))
    model.add(LSTM(units=50))
    model.add(Dense(1))
    model.compile(optimizer='adam', loss='mean_squared_error')
    return model

# 4. Train & Predict
def train_and_predict(ticker):
    df = fetch_stock_data(ticker)
    X, y, scaler = prepare_data(df)

    model = build_lstm_model((X.shape[1], 1))
    model.fit(X, y, epochs=10, batch_size=32, verbose=1)

    # Predict next day
    last_60_days = df[-60:].values
    last_60_scaled = scaler.transform(last_60_days)
    X_input = np.reshape(last_60_scaled, (1, 60, 1))
    predicted_price_scaled = model.predict(X_input)
    predicted_price = scaler.inverse_transform([[predicted_price_scaled[0][0]]])[0][0]

    print(f"\nðŸ“ˆ Predicted next-day closing price for {ticker}: ${predicted_price:.2f}")

    # Plot actual vs predicted
    predicted_prices = model.predict(X)
    predicted_prices = scaler.inverse_transform(predicted_prices)
    actual_prices = scaler.inverse_transform(y.reshape(-1, 1))

    plt.figure(figsize=(10, 6))
    plt.plot(actual_prices, label='Actual Price')
    plt.plot(predicted_prices, label='Model Prediction')
    plt.title(f"{ticker} Price Prediction")
    plt.xlabel("Time")
    plt.ylabel("Price")
    plt.legend()
    plt.show()

# Run it
if __name__ == "__main__":
    ticker = input("Enter Stock Ticker (e.g., AAPL, TSLA): ").upper()
    train_and_predict(ticker)

